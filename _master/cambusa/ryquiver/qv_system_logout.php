function qv_system_logout($maestro, $data){
    global $babelcode, $babelparams;
    try{
        // IMPOSTO I VALORI DI RITORNO PREDEFINITI
        $success=1;
        $message="Operazione riuscita";
        $SYSID="";
        
        // DETERMINO IL CODICE SESSIONE
        if(isset($data["SESSIONID"])){
            $SESSIONID=ryqEscapize($data["SESSIONID"]);
            // CANCELLO LA SESSIONE DALLA CACHE
            $sql="DELETE FROM QVSESSIONS WHERE SESSIONID='$SESSIONID'";
            maestro_execute($maestro, $sql, false);
        }
    }
    catch(Exception $e){
        $success=0;
        $message=$e->getMessage();
    }
    // USCITA JSON
    $j=array();
    $j["success"]=$success;
    $j["code"]=$babelcode;
    $j["params"]=$babelparams;
    $j["message"]=$message;
    $j["SYSID"]=$SYSID;
    return $j; //ritorno standard
}
?>