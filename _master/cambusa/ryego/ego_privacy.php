<b>Cosa sono i cookie?</b><br>
I cookie sono file di testo salvati sul tuo computer allo scopo di memorizzare dati per un loro riutilizzo successivo. <br>
<br>
<b>Come usiamo i cookie</b><br>
Analisi: vengono tracciate in modo anonimo le pagine visitate a scopo statistico.<br>
Impostazioni: alcune scelte effettuate durante la navigazione sono memorizzate per essere riproposte automaticamente.<br>
Sicurezza: eventuali meccanismi di autenticazione non possono funzionare senza i cookie.<br>
<i>I cookie che abbiamo impostato non sono visibili all'esterno del sito e non sono utilizzati a scopo pubblicitario.</i><br>
<br>
<b>Le tue scelte riguardo ai cookie</b><br>
Puoi scegliere di visualizzare un avviso da parte del computer ogni volta che si installa un cookie, 
oppure di disattivare tutti i cookie.<br>
Puoi impostare queste preferenze tra le opzioni del tuo browser. <br>
<i>Disattivando i cookie, però, non sarai più in grado di accedere al sistema.</i><br>
