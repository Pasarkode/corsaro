CREATE VIEW QW_PERSONE AS 
SELECT 
    QVOBJECTS.SYSID,
    QVOBJECTS.NAME,
    QVOBJECTS.DESCRIPTION,
    QVOBJECTS.REGISTRY,
    QVOBJECTS.TYPOLOGYID,
    QVOBJECTS.REFGENREID,
    QVOBJECTS.REFOBJECTID,
    QVOBJECTS.REFQUIVERID,
    QVOBJECTS.BEGINTIME,
    QVOBJECTS.ENDTIME,
    QVOBJECTS.REFERENCE,
    QVOBJECTS.AUXTIME,
    QVOBJECTS.AUXAMOUNT,
    QVOBJECTS.TAG,
    QVOBJECTS.CONSISTENCY,
    QVOBJECTS.SCOPE,
    QVOBJECTS.UPDATING,
    QVOBJECTS.DELETING,
    QVOBJECTS.ROLEID,
    QVOBJECTS.USERINSERTID,
    QVOBJECTS.USERUPDATEID,
    QVOBJECTS.USERDELETEID,
    QVOBJECTS.TIMEINSERT,
    QVOBJECTS.TIMEUPDATE,
    QVOBJECTS.TIMEDELETE,
    OBJECTS_PERSONE.SYSID AS EXTENSIONID,
    OBJECTS_PERSONE.NOME AS NOME,
    OBJECTS_PERSONE.COGNOME AS COGNOME,
    OBJECTS_PERSONE.SESSO AS SESSO,
    OBJECTS_PERSONE.INDIRIZZO AS INDIRIZZO,
    OBJECTS_PERSONE.CIVICO AS CIVICO,
    OBJECTS_PERSONE.CITTA AS CITTA,
    OBJECTS_PERSONE.CAP AS CAP,
    OBJECTS_PERSONE.PROVINCIA AS PROVINCIA,
    OBJECTS_PERSONE.NAZIONE AS NAZIONE,
    OBJECTS_PERSONE.CODFISC AS CODFISC,
    OBJECTS_PERSONE.PIVA AS PIVA,
    OBJECTS_PERSONE.DOCUMENTO AS DOCUMENTO,
    OBJECTS_PERSONE.TESSERA AS TESSERA,
    OBJECTS_PERSONE.LUOGONASCITA AS LUOGONASCITA,
    OBJECTS_PERSONE.LUOGOMORTE AS LUOGOMORTE,
    OBJECTS_PERSONE.CITTADINANZA AS CITTADINANZA,
    OBJECTS_PERSONE.EMAIL AS EMAIL,
    OBJECTS_PERSONE.TELEFONO AS TELEFONO,
    OBJECTS_PERSONE.CELLULARE AS CELLULARE,
    OBJECTS_PERSONE.FAX AS FAX,
    OBJECTS_PERSONE.CONTODEFAULTID AS CONTODEFAULTID
FROM QVOBJECTS 
LEFT JOIN OBJECTS_PERSONE ON OBJECTS_PERSONE.SYSID=QVOBJECTS.SYSID 
WHERE 
    QVOBJECTS.TYPOLOGYID=[:SYSID(0PERSONE000000)] AND QVOBJECTS.DELETED=0
