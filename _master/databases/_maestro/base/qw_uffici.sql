CREATE VIEW QW_UFFICI AS 
SELECT 
    QVOBJECTS.SYSID,
    QVOBJECTS.NAME,
    QVOBJECTS.DESCRIPTION,
    QVOBJECTS.REGISTRY,
    QVOBJECTS.TYPOLOGYID,
    QVOBJECTS.REFGENREID,
    QVOBJECTS.REFOBJECTID,
    QVOBJECTS.REFQUIVERID,
    QVOBJECTS.BEGINTIME,
    QVOBJECTS.ENDTIME,
    QVOBJECTS.REFERENCE,
    QVOBJECTS.AUXTIME,
    QVOBJECTS.AUXAMOUNT,
    QVOBJECTS.TAG,
    QVOBJECTS.CONSISTENCY,
    QVOBJECTS.SCOPE,
    QVOBJECTS.UPDATING,
    QVOBJECTS.DELETING,
    QVOBJECTS.ROLEID,
    QVOBJECTS.USERINSERTID,
    QVOBJECTS.USERUPDATEID,
    QVOBJECTS.USERDELETEID,
    QVOBJECTS.TIMEINSERT,
    QVOBJECTS.TIMEUPDATE,
    QVOBJECTS.TIMEDELETE,
    OBJECTS_UFFICI.SYSID AS EXTENSIONID,
    OBJECTS_UFFICI.COLLOCAZIONE AS COLLOCAZIONE,
    OBJECTS_UFFICI.INDIRIZZO AS INDIRIZZO,
    OBJECTS_UFFICI.CIVICO AS CIVICO,
    OBJECTS_UFFICI.CAP AS CAP,
    OBJECTS_UFFICI.CITTA AS CITTA,
    OBJECTS_UFFICI.PROVINCIA AS PROVINCIA,
    OBJECTS_UFFICI.NAZIONE AS NAZIONE,
    OBJECTS_UFFICI.TELEFONO AS TELEFONO,
    OBJECTS_UFFICI.CELLULARE AS CELLULARE,
    OBJECTS_UFFICI.FAX AS FAX,
    OBJECTS_UFFICI.EMAIL AS EMAIL,
    OBJECTS_UFFICI.AZIENDAID AS AZIENDAID,
    OBJECTS_UFFICI.PROPRIETAID AS PROPRIETAID,
    OBJECTS_UFFICI.CONTODEFAULTID AS CONTODEFAULTID,
    OBJECTS_UFFICI.RESPONSABILEID AS RESPONSABILEID 
FROM QVOBJECTS 
LEFT JOIN OBJECTS_UFFICI ON OBJECTS_UFFICI.SYSID=QVOBJECTS.SYSID 
WHERE 
    QVOBJECTS.TYPOLOGYID=[:SYSID(0UFFICI0000000)] AND QVOBJECTS.DELETED=0

