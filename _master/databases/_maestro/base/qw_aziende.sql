CREATE VIEW QW_AZIENDE AS 
SELECT 
    QVOBJECTS.SYSID,
    QVOBJECTS.NAME,
    QVOBJECTS.DESCRIPTION,
    QVOBJECTS.REGISTRY,
    QVOBJECTS.TYPOLOGYID,
    QVOBJECTS.REFGENREID,
    QVOBJECTS.REFOBJECTID,
    QVOBJECTS.REFQUIVERID,
    QVOBJECTS.BEGINTIME,
    QVOBJECTS.ENDTIME,
    QVOBJECTS.REFERENCE,
    QVOBJECTS.AUXTIME,
    QVOBJECTS.AUXAMOUNT,
    QVOBJECTS.TAG,
    QVOBJECTS.CONSISTENCY,
    QVOBJECTS.SCOPE,
    QVOBJECTS.UPDATING,
    QVOBJECTS.DELETING,
    QVOBJECTS.ROLEID,
    QVOBJECTS.USERINSERTID,
    QVOBJECTS.USERUPDATEID,
    QVOBJECTS.USERDELETEID,
    QVOBJECTS.TIMEINSERT,
    QVOBJECTS.TIMEUPDATE,
    QVOBJECTS.TIMEDELETE,
    OBJECTS_AZIENDE.SYSID AS EXTENSIONID,
    OBJECTS_AZIENDE.INDIRIZZO AS INDIRIZZO,
    OBJECTS_AZIENDE.CIVICO AS CIVICO,
    OBJECTS_AZIENDE.CAP AS CAP,
    OBJECTS_AZIENDE.CITTA AS CITTA,
    OBJECTS_AZIENDE.PROVINCIA AS PROVINCIA,
    OBJECTS_AZIENDE.NAZIONE AS NAZIONE,
    OBJECTS_AZIENDE.CODFISC AS CODFISC,
    OBJECTS_AZIENDE.PIVA AS PIVA,
    OBJECTS_AZIENDE.CCIAA AS CCIAA,
    OBJECTS_AZIENDE.ATECO AS ATECO,
    OBJECTS_AZIENDE.ABI AS ABI,
    OBJECTS_AZIENDE.CAB AS CAB,
    OBJECTS_AZIENDE.TELEFONO AS TELEFONO,
    OBJECTS_AZIENDE.CELLULARE AS CELLULARE,
    OBJECTS_AZIENDE.FAX AS FAX,
    OBJECTS_AZIENDE.EMAIL AS EMAIL,
    OBJECTS_AZIENDE.BANCA AS BANCA,
    OBJECTS_AZIENDE.CLIENTE AS CLIENTE,
    OBJECTS_AZIENDE.FORNITORE AS FORNITORE,
    OBJECTS_AZIENDE.PROPRIETA AS PROPRIETA,
    OBJECTS_AZIENDE.CONTODEFAULTID AS CONTODEFAULTID,
    OBJECTS_AZIENDE.TITOLAREID AS TITOLAREID 
FROM QVOBJECTS 
LEFT JOIN OBJECTS_AZIENDE ON OBJECTS_AZIENDE.SYSID=QVOBJECTS.SYSID 
WHERE 
    QVOBJECTS.TYPOLOGYID=[:SYSID(0AZIENDE000000)] AND QVOBJECTS.DELETED=0
